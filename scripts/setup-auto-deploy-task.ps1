$action = New-ScheduledTaskAction -Execute 'pwsh.exe' -Argument '-File "C:\Users\erich\Heady\scripts\auto-deploy-orchestrator.ps1"'
$trigger = New-ScheduledTaskTrigger -Daily -At 3am
