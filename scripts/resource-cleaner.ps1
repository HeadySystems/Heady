param([switch]$Aggressive)

# Cleanup priorities
$cleanupTargets = @(
    "*tsc*",
    "*python*",
    "*docker*"
)

if ($Aggressive) {
    $cleanupTargets = [System.Collections.ArrayList]@("*node*", "*java*", "*chrome*")
}

Get-Process | Where-Object {
    $proc = $_
    $cleanupTargets | Where-Object { $proc.ProcessName -like $_ }
} | Stop-Process -Force
