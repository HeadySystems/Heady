# Script to inject Soul-Token into VM image
param(
    [Parameter(Mandatory=$true)]
    [string]$Image,
    
    [Parameter(Mandatory=$true)]
    [string]$Token
)

# Mount VHDX
$mountResult = Mount-VHD -Path $Image -PassThru
$disk = $mountResult | Get-Disk
$partition = $disk | Get-Partition
$driveLetter = $partition.DriveLetter

# Create token file
Set-Content -Path "${driveLetter}:\Heady\soul-token.txt" -Value $Token

# Dismount VHDX
Dismount-VHD -Path $Image
