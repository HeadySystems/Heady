<#
.SYNOPSIS
Generates 1000+ improvement tasks locally
#>

# Get project context
$context = Get-Content "$PSScriptRoot\..\project-context.json" | ConvertFrom-Json

# Generate tasks
$tasks = @()
for ($i=1; $i -le 1000; $i++) {
    $tasks += @{
        id = "task$i"
        title = "Improvement #$i"
        area = $context.improvementAreas | Get-Random
        module = $context.modules | Get-Random
        priority = Get-Random -Minimum 1 -Maximum 10
    }
}

# Save tasks
$tasks | ConvertTo-Json | Set-Content "$PSScriptRoot\..\data\improvement-tasks.json"

Write-Host "Generated $($tasks.Count) improvement tasks"
