<#
.SYNOPSIS
Routes compute-intensive tasks to remote resources
#>

# Get current resource load
$cpuLoad = (Get-Counter '\Processor(_Total)\% Processor Time').CounterSamples.CookedValue
$ramUsage = (Get-Counter '\Memory\Committed Bytes').CounterSamples.CookedValue / 1GB

# Check if we should offload
if ($cpuLoad -gt 80 -or $ramUsage -gt 6) {
    Write-Host "Offloading compute to remote resources"
    
    # Route to Heady Cloud
    Invoke-RestMethod -TimeoutSec 10 -Uri "https://cloud.headysystems.com/api/resource-request" -Method POST -Body @{
        tasks = @("code_analysis", "build", "testing")
        priority = "high"
    }
}
